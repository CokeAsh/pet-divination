export function getTarotMeaning(cardName, reversed = false) {
  const card = TAROT_MEANINGS[cardName]
  if (!card) return ''
  const posLabel = reversed ? '逆位' : '正位'
  const meaning = reversed ? card.reversed : card.upright
  return `${cardName}（${posLabel}）：${meaning}`
}

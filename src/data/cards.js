export function getDeck(type) {
  return type === 'tarot' ? [...TAROT_MAJOR] : [...LENORMAND]
}

export function drawCards(type, count) {
  const pool = getDeck(type)
  const result = []

  for (let i = 0; i < count && pool.length > 0; i += 1) {
    const index = Math.floor(Math.random() * pool.length)
    const card = { ...pool.splice(index, 1)[0] }
    card.reversed = type === 'tarot' ? Math.random() < 0.5 : false
    result.push(card)
  }

  return result
}

export function finalizeSelectedCards(type, selectedCards) {
  return selectedCards.map((card) => ({
    ...card,
    reversed: type === 'tarot' ? Math.random() < 0.5 : false,
  }))
}
